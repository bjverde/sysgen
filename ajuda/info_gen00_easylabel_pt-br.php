EasyLabel é um recurso que irá alterar a Label dos campos das telas FormDin. Diminuindo seu trabalho de alterar as labels, no momento conta com regras fixas. Se o campo da tabela coincidir com o Tipo e a Regex o label será alterado. Exemplos: 
<ul>
	<li>Tipo Numerico e nome do campo QTALGO = Quantidade Algo</li>
	<li>Tipo Data e nome do campo DTALGO = Data Algo</li>
	<li>Tipo Texto e nome do campo MNALGO = Nome Algo</li>
	<li>Tipo Texto e nome do campo DSALGO = Descrição Algo</li>
</ul>